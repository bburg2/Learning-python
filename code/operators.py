# you can work with basic operators
number = 1 + 2 * 3 / 4.0
print(number)

# the modulo (%) operator returns the remainder of a division
remainder = 11 % 3
print(remainder)

# tow multiplication symbols (**) makes it the power of
squared = 6 ** 2
cubed = 2 ** 3
print(squared)
print(cubed)

# you can add strings together with the help of (+)
helloworld = "hello" + " " + "world"
print(helloworld)

# you can also multiply a string to show multiple strings added together
lotsofbananas = "banana" * 10
print(lotsofbananas)

# you can also use operators on lists
even_numbers = [2,4,6,8]
odd_numbers = [1,3,5,7]
all_numbers = odd_numbers + even_numbers
print(all_numbers)

# you can also multiply a list
print([1,2,3] * 3)


